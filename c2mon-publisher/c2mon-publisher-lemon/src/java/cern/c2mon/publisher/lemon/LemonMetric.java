package cern.c2mon.publisher.lemon;

public class LemonMetric
{

	private String name;
	private Object value;
	private long timestamp;
	
	public LemonMetric(String name, Object value, long timestamp)
	{
		this.name = name;
		this.value = value;
		this.timestamp = timestamp;
	}



	public String getName()
	{
		return name;
	}



	public Object getValue()
	{
		return value;
	}



	public long getTimestamp()
	{
		return timestamp;
	}



	@Override
	public int hashCode()
	{
		final int prime = 31;
		int result = 1;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		result = prime * result + (int) (timestamp ^ (timestamp >>> 32));
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj)
	{
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		LemonMetric other = (LemonMetric) obj;
		if (name == null)
		{
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (timestamp != other.timestamp)
			return false;
		if (value == null)
		{
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}
}
